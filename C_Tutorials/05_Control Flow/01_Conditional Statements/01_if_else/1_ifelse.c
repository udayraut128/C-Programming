// 2.Example

#include <stdio.h>

int main()
{
    int num = -5;

    if (num > 0)
    {
        printf("The number is positive.\n");
    }
    else
    {
        printf("The number is not positive.\n");
    }

    return 0;
}

// output
// The number is not positive.