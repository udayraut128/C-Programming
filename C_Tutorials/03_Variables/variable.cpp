#include <iostream>
using namespace std;

int main()
{
    int myInt = 10;
    double myDouble = 3.14;
    char myChar = 'A';
    bool myBool = true;
    string myString = "Hello, C++";

    cout << "Integer: " << myInt << endl;
    cout << "Double: " << myDouble << endl;
    cout << "Char: " << myChar << endl;
    cout << "Boolean: " << myBool << endl;
    cout << "String: " << myString << endl;

    return 0;
}

// output
// Integer: 10
// Double: 3.14
// Char: A
// Boolean: 1
// String: Hello, C++