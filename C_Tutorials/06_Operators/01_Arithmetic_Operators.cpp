#include <iostream>
using namespace std;

int main()
{
    int a = 10, b = 5;
    cout << "a + b = " << (a + b) << endl; // Addition
    cout << "a - b = " << (a - b) << endl; // Subtraction
    cout << "a * b = " << (a * b) << endl; // Multiplication
    cout << "a / b = " << (a / b) << endl; // Division
    cout << "a % b = " << (a % b) << endl; // Modulus
    return 0;
}

// output
// a + b = 15
// a - b = 5
// a * b = 50
// a / b = 2
// a % b = 0