#include <stdio.h>

int main()
{
    int a = 10, b = 3;

    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    printf("a %% b = %d\n", a % b);

    return 0;
}

// output
// a + b = 13
// a - b = 7
// a * b = 30
// a / b = 3
// a % b = 1